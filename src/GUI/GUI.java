     private void darValores(ArrayList<Atributo> lista, JComboBox box, int n) {
        ArrayList<String> nombres = lista.get(n).getNombres();
        for(int i = 0; i < nombres.size(); i++){
            box.addItem(nombres.get(i));
        }
    }
